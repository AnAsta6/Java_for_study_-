package ru.example.lab.geometry;

/**
 * Класс, реализующий геометрическую фигуру "Треугольник".
 * Хранит длины сторон треугольника и предоставляет методы для вычисления площади.
 */
public class Triangle implements Shape {
    // Длина первой стороны треугольника
    private double a;
    // Длина второй стороны треугольника
    private double b;
    // Длина третьей стороны треугольника
    private double c;

    /**
     * Создает новый треугольник с заданными длинами сторон.
     *
     * @param a длина первой стороны треугольника
     * @param b длина второй стороны треугольника
     * @param c длина третьей стороны треугольника
     */
    public Triangle(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    /**
     * Вычисляет площадь треугольника по формуле Герона.
     * Формула: sqrt(s*(s-a)*(s-b)*(s-c)), где s - полупериметр.
     *
     * @return площадь треугольника
     */
    @Override
    public double area() {
        double s = (a + b + c) / 2; // Полупериметр
        return Math.sqrt(s * (s - a) * (s - b) * (s - c)); // Площадь по формуле Герона
    }

    /**
     * Возвращает строковое представление треугольника.
     *
     * @return строка с описанием сторон и площади треугольника
     */
    @Override
    public String toString() {
        return "Треугольник со сторонами " + a + ", " + b + ", " + c + " и площадью " + area();
    }
}